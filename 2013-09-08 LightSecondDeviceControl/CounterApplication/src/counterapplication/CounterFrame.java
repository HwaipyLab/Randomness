    private String formatCounterString(int value) {
        String s = "";
        int tail = value % 1000;
        value /= 1000;
        s += tail;
        while (value != 0) {
            tail = value % 1000;
            value /= 1000;
            s = tail + "," + s;
        }
        return s;
    }
